// Q7 - Type Casting Example
public class Q07_TypeCastingExample {
    public static void main(String[] args) {
        double d = 9.78;
        int i = (int) d;
        System.out.println("double to int: " + d + " -> " + i);

        int x = 5;
        double y = (double) x;
        System.out.println("int to double: " + x + " -> " + y);
    }
}
